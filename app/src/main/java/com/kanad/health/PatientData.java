package com.kanad.health;

public class PatientData {
    String uid;
    String name_;
    String gender_;
    String age_;
    String imageurl;

    public String getUid() {
        return uid;
    }

    public String getName_() {
        return name_;
    }

    public String getGender_() {
        return gender_;
    }

    public String getAge_() {
        return age_;
    }

    public String getImageurl() {
        return imageurl;
    }
}
